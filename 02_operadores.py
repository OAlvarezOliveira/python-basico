# Operadores aritméticos
print( 3 + 4) 
print( 3 - 4)
print( 3 * 4)
print( 3 / 4) 
print( 10 % 3) 
print( 10 // 3)
print( 10 ** 3)

# Operadores comparativos
print( 3 > 4) 
print( 3 < 4)
print( 3 >= 4) 
print( 3 <= 4)
print( 3 == 4)
print( 3 != 4) 

# Operadores lógicos
print( 3 > 4 and "Hola" > "Python") 
print( 3 > 4 or "Hola" > "Python")
print( 3 < 4 or "Hola" < "Python")
print( 3 < 4 or "Hola" > "Python")
print(not("Hola" > "Python")) 
 
          